    private void volver() {
        RelacionVendeListaPorceFrame rvlpf = new RelacionVendeListaPorceFrame();
        rvlpf.setVisible(true);
        this.dispose();
    }

    private void guardar() {
        porcentual.setPorcentual(Float.valueOf(porcentualTxt.getText()));
        try {
            new PorcentualVendedorService().updatePorcentualVendedor(porcentual);
        } catch (Exception ex) {
            Logger.getLogger(ModificarRelacionFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        volver();
    }

    private void completarCampos() {
        try {
            porcentual = new PorcentualVendedorService().getPorcentualVendedorById(id);
        } catch (Exception ex) {
            Logger.getLogger(ModificarRelacionFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "No se pudo acceder al registro");
            volver();
        }
        vendedorTxt.setText(porcentual.getVendedor().getNombre());
        rubroTxt.setText(porcentual.getRubro().getNombre());
        porcentualTxt.setText(df.format(porcentual.getPorcentual()));
        vendedorTxt.setEditable(false);
        rubroTxt.setEditable(false);
        porcentualTxt.requestFocus();
    }
