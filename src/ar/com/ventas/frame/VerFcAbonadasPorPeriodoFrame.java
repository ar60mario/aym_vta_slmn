    private void buscar() {
        limpiarTabla();
        Date de = new Date();
        Date al = new Date();
        try {
            de = sdf.parse(desdeTxt.getText());
            al = sdf.parse(hastaTxt.getText());
        } catch (ParseException ex) {
            Logger.getLogger(VerFcAbonadasPorPeriodoFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        List<ComprobanteCompras> comprobantes = null;
        try {
            comprobantes = new ComprobanteComprasService().getComprobantesPagosEntreFechasFactura(de, al);
        } catch (Exception ex) {
            Logger.getLogger(VerFcAbonadasPorPeriodoFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (comprobantes != null && !comprobantes.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (ComprobanteCompras c : comprobantes) {
                Object ob[] = new Object[8];
                ob[0] = sdf.format(c.getFechaFactura());
                if (c.getEsDebito() != null) {
                    if (c.getEsDebito()) {
                        ob[1] = c.getTipoComprobante();
                    } else {
                        ob[1] = "NC";
                    }
                } else {
                    ob[1] = c.getTipoComprobante();
                }
                String l = c.getLetra();
                String s = dfs.format(c.getNumeroSucursal());
                String n = dfn.format(c.getNumeroFactura());
                ob[2] = l + " " + s + "-" + n;
                ob[3] = c.getProveedor().getCodigo();
                ob[4] = c.getProveedor().getRazonSocial();
                ob[5] = df.format(c.getTotal());
                ob[6] = df.format(c.getPagado());
                ob[7] = df.format(c.getTotal() - c.getPagado());
                tbl.addRow(ob);
            }
            tabla.setModel(tbl);
        }
    }

    private void volver() {
        MainFrame mf = new MainFrame();
        mf.setVisible(true);
        this.dispose();
    }

    private void desde() {
        if (desdeTxt.getText().isEmpty()) {
            desdeTxt.setText(sdf.format(new Date()));
            desdeTxt.requestFocus();
        } else {
            int largo = desdeTxt.getText().length();
            if (largo == 2) {
                Calendar cal = Calendar.getInstance();
                int mes = cal.get(Calendar.MONTH) + 1;
                int anio = cal.get(Calendar.YEAR);
                String an = String.valueOf(anio);
                String f = "0" + String.valueOf(mes);
                if (f.length() > 2) {
                    f = f.substring(1, 3);
                }
                f = f + "/" + an;
                desdeTxt.setText(desdeTxt.getText() + "/" + f);
                hastaTxt.requestFocus();
            } else {
                if (largo == 5) {
                    Calendar cal = Calendar.getInstance();
                    int anio = cal.get(Calendar.YEAR);
                    String an = "/" + String.valueOf(anio);
                    desdeTxt.setText(desdeTxt.getText() + an);
                    hastaTxt.requestFocus();
                } else {
                    if (largo != 10) {
                        JOptionPane.showMessageDialog(this, "Error en fecha");
                        desdeTxt.setText("");
//                          request focus mismo campo
                        desdeTxt.requestFocus();
                    } else {
                        String veinte = desdeTxt.getText().substring(6, 8);
                        if (!veinte.equals("20")) {
                            JOptionPane.showMessageDialog(this, "Error en AÑO");
//                              request focus mismo campo
                            desdeTxt.requestFocus();
                        } else {
                            hastaTxt.requestFocus();
                        }
                    }
                }
            }

        }
    }

