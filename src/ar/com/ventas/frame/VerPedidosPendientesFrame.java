    private void llenarTabla() {
        List<Request> pe = null;
        try {
            pe = new RequestService().getAllRequestsDisponibles();
        } catch (Exception ex) {
            Logger.getLogger(VerPedidosPendientesFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (pe != null && !pe.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tablaPedidos.getModel();
            for (Request re: pe) {
                Object ob[] = new Object[3];
                ob[0] = sdf.format(re.getFecha());
                ob[1] = re.getCliente().getCodigo();
                ob[2] = df.format(re.getTotal());
                tbl.addRow(ob);
            }
            tablaPedidos.setModel(tbl);
        }
    }
