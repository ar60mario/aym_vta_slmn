    private void go() {
        List<Cliente> clientex = null;
        try {
            clientex = new ClienteService().getAllClientes();
        } catch (Exception ex) {
            Logger.getLogger(A0test_clientesNew.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (Cliente cx : clientex) {
            String cod = cx.getCodigo();
            Cliente cli = null;
            try {
                System.out.println(cod);
                cli = new ClienteService().getClienteByCodigo(cod);
            } catch (Exception ex) {
                Logger.getLogger(A0test_clientesNew.class.getName()).log(Level.SEVERE, null, ex);
            }
            CtaCteCliente ccc = null;
            try {
                ccc = new CtaCteClienteService().getUltimoMovimiento(cli);
                String a1 = df.format(cli.getSaldo());
                String a2 = df.format(ccc.getSaldo());
                if (!a1.equals(a2)) {
                    JOptionPane.showMessageDialog(this, "dife "
                            + cli.getCodigo() + " "
                            + a1 + " "
                            + a2);
                }
            } catch (Exception ex) {
                Logger.getLogger(A0test_clientesNew.class.getName()).log(Level.SEVERE, null, ex);
                System.out.println(cli.getCodigo());
                System.out.println(cli.getSaldo());
//                JOptionPane.showMessageDialog(this, cli.getCodigo());
//                JOptionPane.showMessageDialog(this, cli.getSaldo());
                continue;
            }
            //System.out.println(ccc.getCliente().getRazonSocial());

//            if (ccc != null) {
//                System.out.println(df.format(ccc.getSaldo()));
//                System.out.println(df.format(cli.getSaldo()));
//                System.out.println(ccc.getId());
//                System.out.println(ccc.getCliente().getCodigo());
//            }
            Customer cus = null;
            try {
                cus = new CustomerService().getCustomerByCodigo(cod);

            } catch (Exception ex) {
                Logger.getLogger(A0test_clientesNew.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (cus != null) {
                try {
                    Inventory cct = new InventoryService().getUltimoMovimiento(cus);
                    String a1 = df.format(cus.getSaldo());
                    String a2 = df.format(cct.getSaldo());
                    if (!a1.equals(a2)) {
                        JOptionPane.showMessageDialog(this, "dife "
                                + cus.getCodigo() + " "
                                + a1 + " "
                                + a2);
                    }
                } catch (Exception ex) {
                    Logger.getLogger(A0test_clientesNew.class.getName()).log(Level.SEVERE, null, ex);
                    System.out.println(cus.getCodigo());
                    System.out.println(cus.getSaldo());
                    continue;
                }
            }
        }
    }
