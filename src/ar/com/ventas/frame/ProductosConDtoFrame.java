    private void modificar() {
        if (!cantidadTxt.getText().isEmpty()) {
            int cant = Integer.valueOf(cantidadTxt.getText());
            if (!porcentajeTxt.getText().isEmpty()) {
                int porc = Integer.valueOf(porcentajeTxt.getText());
                int rows = tabla.getRowCount();
                int x[] = tabla.getSelectedRows();
                int ca = tabla.getSelectedRows().length;
                System.out.println(tabla.getSelectedRows().length);
                for (int i = 0; i < ca; i++) {
                    System.out.println(x[i]);
                    Producto pro = productos.get(x[i]);
                    pro.setCantidadDto(cant);
                    pro.setDescuento(porc);
                    try {
                        new ProductoService().updateProducto(pro);
                    } catch (Exception ex) {
                        Logger.getLogger(ProductosConDtoFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                borrarTabla();
                llenarTabla();
            }
        }
    }

    private void llenarTabla() {
        cantidadTxt.setText("");
        porcentajeTxt.setText("");
        productos = null;
        try {
            productos = new ProductoService().getAllProductosActivosConDescuento();
        } catch (Exception ex) {
            Logger.getLogger(ProductosConDtoFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (productos != null && !productos.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Producto pr : productos) {
                Object ob[] = new Object[5];
                ob[0] = pr.getCodigo();
                ob[1] = pr.getDetalle();
                ob[2] = df.format(pr.getPrecio());
                if (pr.getDescuento() != null) {
                    ob[3] = dfi.format(pr.getDescuento());
                } else {
                    ob[3] = dfi.format(0);
                }
                if (pr.getCantidadDto() != null) {
                    ob[4] = dfi.format(pr.getCantidadDto());
                } else {
                    ob[4] = dfi.format(0);
                }
                tbl.addRow(ob);
            }
            tabla.setModel(tbl);
        }
    }

    private void borrarTabla() {
        int rows = tabla.getRowCount();
        if(rows > 0){
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for(int i = 0;i<rows;i++){
                tbl.removeRow(0);
            }
            tabla.setModel(tbl);
        }
    }
