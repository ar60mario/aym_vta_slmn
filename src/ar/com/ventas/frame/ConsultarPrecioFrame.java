    private void limpiarCampos() {
        nombreProductoTxt.setText("");
        precioProductoTxt.setText("");
        codigoProductoTxt.setText("");
        comboProductos.removeAllItems();
        comboProductos.addItem("");
        nombreProductoEncontradoTxt.setText("");
        sugeridoTxt.setText("");
        nombreProductoTxt.requestFocus();
    }

    private void limpiar2(){
        nombreProductoTxt.setText("");
        comboProductos.removeAllItems();
        comboProductos.addItem("");
    }
    
    private void buscarPorFiltro() {
        String filtro = nombreProductoTxt.getText();
        limpiarCampos();
        productos = null;
        try {
            productos = new ProductoService().getProductosByFiltro(filtro);
        } catch (Exception ex) {
            Logger.getLogger(ConsultarPrecioFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (productos != null && !productos.isEmpty()) {
            for (Producto producto : productos) {
                comboProductos.addItem(producto.getDetalle());
            }
            comboProductos.requestFocus();
            comboProductos.addFocusListener(null);
            comboProductos.showPopup();
        }
    }

    private void buscarPorCodigo() {
        int codigo = Integer.valueOf(codigoProductoTxt.getText());
        Producto producto = null;
        Configuracion config = null;
        Long lon = 1L;
        try {
            producto = new ProductoService().getProductoByCodigo(codigo);
            config = new ConfiguracionService().getFacturas(lon);
        } catch (Exception ex) {
            Logger.getLogger(ConsultarPrecioFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (producto != null) {
            //if (!producto.getSubRubro().getCodigo().equals(5099)) {
                nombreProductoEncontradoTxt.setText(producto.getDetalle());
                Float iva = config.getIva();
                Double precio = producto.getPrecio() * (1 + (iva / 100)) + producto.getImpuesto();
                precioProductoTxt.setText(df.format(precio));
                sugeridoTxt.setText(df.format(producto.getSugerido()));
            //}
        } else {
            JOptionPane.showMessageDialog(this, "Producto no encontrado");
            codigoProductoTxt.requestFocus();
        }
    }

    private boolean isNumeric(KeyEvent evt) {
        String cod = String.valueOf(evt.getKeyChar());
        try {
            Integer.parseInt(cod);
        } catch (NumberFormatException ex) {
            return false;
        }
        return true;
    }

    private void mostrarProducto(int row) {
        Producto producto = productos.get(row - 1);
        Configuracion config = null;
        Long lon = 1L;
        try {
            config = new ConfiguracionService().getFacturas(lon);
        } catch (Exception ex) {
            Logger.getLogger(ConsultarPrecioFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        nombreProductoEncontradoTxt.setText(producto.getDetalle());
        codigoProductoTxt.setText(producto.getCodigo().toString());
        Float iva = config.getIva();
        Double precio = producto.getPrecio() * (1 + (iva / 100)) + producto.getImpuesto();
        precioProductoTxt.setText(df.format(precio));
        sugeridoTxt.setText(df.format(producto.getSugerido()));
    }

    private void volver() {
        MainFrame mf = new MainFrame();
        mf.setVisible(true);
        this.dispose();
    }
