    private void llenarCampos() {
        proveedorTxt.setText(cc1.getProveedor().getRazonSocial());
        String f = cc1.getLetra()
                + " " + dfs.format(cc1.getSucursal())
                + "-" + dfn.format(cc1.getNumero());
        facturaTxt.setText(f);
        fechaTxt.setText(sdf.format(cc1.getFecha()));
        codigoTxt.setText(cc1.getProducto().getCodigo().toString());
        detalleTxt.setText(cc1.getProducto().getDetalle());
        cantidadTxt.setText(df.format(cc1.getCantidad()));
        filtroTxt.setText("");
        combo.removeAllItems();
        combo.addItem("");
        productoCodigoTxt.setText("");
        productoDetalleTxt.setText("");
        nuevaCantidadTxt.setText("");
        filtroTxt.requestFocus();
    }

    private void buscarP() {
        String filtro = filtroTxt.getText();
        if (filtro.isEmpty()) {
            return;
        }
        productos = null;
        try {
            productos = new ProductoService().getProductosByFiltro(filtro);
        } catch (Exception ex) {
            Logger.getLogger(ModificarStockFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (productos != null && !productos.isEmpty()) {
            for (Producto pr : productos) {
                combo.addItem(pr.getDetalle());
            }
            combo.addFocusListener(null);
            combo.showPopup();
            combo.requestFocus();
        }
    }

    private void cargarP() {
        int row = combo.getSelectedIndex();
        if (row < 1) {
            return;
        }
        Producto pro = productos.get(row - 1);
        productoCodigoTxt.setText(pro.getCodigo().toString());
        productoDetalleTxt.setText(pro.getDetalle());
        nuevaCantidadTxt.requestFocus();
    }

    private void grabar() {
        int a = JOptionPane.showConfirmDialog(this, "CONFIRMA GRABAR LA MODIFICACION?", "AtenciÃ³n", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            if (!nuevaCantidadTxt.getText().isEmpty()) {
                int row = combo.getSelectedIndex();
                if (row < 1) {
                    return;
                }
                Producto pro = productos.get(row - 1);
                Float cantNew = Float.valueOf(nuevaCantidadTxt.getText());
                CtaCteStock cc2 = new CtaCteStock();
                cc2.setCantidad(cantNew);
                cc2.setComprobante(cc1.getComprobante());
                cc2.setFecha(new Date());
                cc2.setLetra(cc1.getLetra());
                cc2.setNumero(cc1.getNumero());
                cc2.setProducto(pro);
                cc2.setProveedor(cc1.getProveedor());
                cc2.setSucursal(cc1.getSucursal());
                cc2.setUsuario(usuario);
                Integer codi = pro.getCodigo();
                Producto prod = null;
                try {
                    prod = new ProductoService().getProductoByCodigo(codi);
                } catch (Exception ex) {
                    Logger.getLogger(ModificarStockFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                Float ne = prod.getStock() + cantNew;
                prod.setStock(ne);
                try {
                    new ProductoService().updateProducto(prod);
                } catch (Exception ex) {
                    Logger.getLogger(ModificarStockFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                codi = cc1.getProducto().getCodigo();
                prod = null;
                try {
                    prod = new ProductoService().getProductoByCodigo(codi);
                } catch (Exception ex) {
                    Logger.getLogger(ModificarStockFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                ne = prod.getStock() - cc1.getCantidad();
                prod.setStock(ne);
                try {
                    new ProductoService().updateProducto(prod);
                } catch (Exception ex) {
                    Logger.getLogger(ModificarStockFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                try {
                    new CtaCteStockService().saveCtaCteStock(cc2);
                } catch (Exception ex) {
                    Logger.getLogger(ModificarStockFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

    private void volver() {
        CuentaCorrienteStockFrame ccsf = new CuentaCorrienteStockFrame(de, al, estado, ccs, usuario);
        ccsf.setVisible(true);
        this.dispose();
    }
