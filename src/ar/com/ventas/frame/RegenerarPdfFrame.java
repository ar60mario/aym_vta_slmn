    private void limpiarCampos() {
        Date fecha = new Date();
        fechaCmpteTxt.setText(sdf.format(fecha));
        llenarTabla();
    }

    private void llenarTabla() {
        if (!fechaCmpteTxt.getText().isEmpty()) {
            Date fecha = new Date();
            try {
                fecha = sdf.parse(fechaCmpteTxt.getText());
            } catch (ParseException ex) {
                Logger.getLogger(RegenerarPdfFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                facturas = new IvaVentasService().getFactByFecha(fecha);
            } catch (Exception ex) {
                Logger.getLogger(RegenerarPdfFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (facturas != null && !facturas.isEmpty()) {
                limpiarTabla();
                DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
                for (IvaVentas fc : facturas) {
                    Object o[] = new Object[4];
                    o[0] = sdf.format(fc.getFecha());
                    o[1] = fc.getLetra() + " " + fc.getNumeroSucursal() + " " + fc.getNumeroFactura();
                    o[2] = fc.getCliente().getRazonSocial();
                    o[3] = df.format(fc.getTotal());
                    tbl.addRow(o);
                }
                tabla.setModel(tbl);
            }
        }
    }

    private void limpiarTabla() {
        int rows = tabla.getRowCount();
        if (rows > 0) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (int i = 0; i < rows; i++) {
                tbl.removeRow(0);
            }
            tabla.setModel(tbl);
        }
    }
