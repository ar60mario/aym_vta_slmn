    private void llenarTabla() {
        DefaultTableModel tabla = (DefaultTableModel) tablaClientes.getModel();
        List<Customer> clientes = null;
        try {
            clientes = new CustomerService().getAllCustomers();
        } catch (Exception ex) {
            Logger.getLogger(ClientesHabilitadosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (clientes != null && !clientes.isEmpty()) {
            for (Customer cli : clientes) {
                Object ob[] = new Object[3];
                ob[0] = cli.getCodigo();
                Cliente clie = null;
                try {
                    clie = new ClienteService().getClienteByCodigo(cli.getCodigo());
                } catch (Exception ex) {
                    Logger.getLogger(ClientesHabilitadosFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                ob[1] = clie.getRazonSocial();
                if (cli.getActivo()) {
                    ob[2] = "Activo";
                } else {
                    ob[2] = "Inactivo";
                }
                tabla.addRow(ob);
            }
            tablaClientes.setModel(tabla);
        }
    }
