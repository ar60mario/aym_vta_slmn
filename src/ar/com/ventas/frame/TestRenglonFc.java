    private void buscarRenglones() {
        String letra = "B";
        int suc = 1;
        int num = 21;
        Activity fc = null;
        try {
            fc = new ActivityService().getFacturaByNumero(letra, suc, num);
        } catch (Exception ex) {
            Logger.getLogger(TestRenglonFc.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (fc == null) {
            JOptionPane.showMessageDialog(this, "Error 1");
            return;
        }
        System.out.println(fc.getFecha());
        System.out.println(fc.getTotal());
        List<Rental> renglones = null;
        try {
            renglones = new RentalService().getRentalFromActivity(fc);
        } catch (Exception ex) {
            Logger.getLogger(TestRenglonFc.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (renglones != null && !renglones.isEmpty()) {
            for (Rental rf : renglones) {
                System.out.println(rf.getTotal());
            }
        } else {
            if (renglones.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Error 2");
            } else {
                JOptionPane.showMessageDialog(this, "Error 3");
            }
        }
    }
