    private void llenar() {
        clientes = null;
        List<ClienteSaldo> clieSal = new ArrayList<>();
        try {
            clientes = new ClienteService().getAllClientes();
        } catch (Exception ex) {
            Logger.getLogger(A0000Test0.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "ERROR 149");
            return;
        }
        DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
        for (Cliente cl : clientes) {
            if (cl.getActivo() != null) {
                String codigo = cl.getCodigo();
                Customer cus = null;
                try {
                    cus = new CustomerService().getCustomerByCodigo(codigo);
                } catch (Exception ex) {
//                    Logger.getLogger(A0000Test0.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (cl.getActivo()) {
                    if (cus != null) {
                        if (cus.getActivo() != null) {
                            if (cus.getActivo()) {
                                ClienteSaldo cs = new ClienteSaldo();
                                cs.setCliente(cl);
                                cs.setCodigo(cl.getCodigo());
                                cs.setCustomer(cus);
                                cs.setSaldoCliente(cl.getSaldo());
                                cs.setSaldoCustomer(cus.getSaldo());
                                if (cl.getSaldo() > 0.00) {
                                    if (cus.getSaldo() < 0.00) {
                                        clieSal.add(cs);
                                    }
                                } else {
                                    if (cl.getSaldo() < 0.00) {
                                        if (cus.getSaldo() > 0.00) {
                                            clieSal.add(cs);
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }
        for (ClienteSaldo clsa : clieSal) {
            Object o[] = new Object[3];
            String codigo = clsa.getCodigo();
            o[0] = codigo;
            String saldo1 = df.format(clsa.getSaldoCliente());
            o[1] = saldo1;
            o[2] = df.format(clsa.getSaldoCustomer());
            tbl.addRow(o);
        }
        tabla.setModel(tbl);
    }

