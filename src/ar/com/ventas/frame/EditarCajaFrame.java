    private void limpiarCampos() {
        fechaTxt.setText(sdf.format(caja.getFecha()));
        fechaTxt.setEditable(false);
        Double arr = 0.00;
        fechaAnteriorTxt.setText(sdf.format(caja.getFechaAnterior()));
        totalFactTxt.setText(df.format(caja.getTotalFacturadoOf()));
        totalFactTxt.setEditable(false);
        totalPresupTxt.setText(df.format(caja.getTotalFacturadoCf()));
        totalPresupTxt.setEditable(false);
        deudCfAntTxt.setText(df.format(caja.getSaldoDeudorAnteriorCf()));

        deudOfAntTxt.setText(df.format(caja.getSaldoDeudorAnteriorOf()));

        arr += caja.getTotalFacturadoOf();
        arr += caja.getTotalFacturadoCf();
        arr += caja.getSaldoDeudorAnteriorCf();
        arr += caja.getSaldoDeudorAnteriorOf();
        subtotalArribaTxt.setText(df.format(arr));
        subtotalArribaTxt.setEditable(false);

        inicialTxt.setText(df.format(caja.getCajaInicial()));
        deudCFhoyTxt.setText(df.format(caja.getTotalDeudoresHoyCf()));
        deudCFhoyTxt.setEditable(false);
        deudOFhoyTxt.setText(df.format(caja.getTotalDeudoresHoyOf()));
        deudOFhoyTxt.setEditable(false);
        efectivoTxt.setText(df.format(caja.getTotalDeposito()));
        valesTxt.setText(df.format(caja.getTotalVales()));
        Double aba = 0.0;
        aba += caja.getCajaInicial();
        aba += caja.getTotalDeudoresHoyCf();
        aba += caja.getTotalDeudoresHoyOf();
        aba += caja.getTotalDeposito();
        aba += caja.getTotalVales();
        subtotalAbajoTxt.setText(df.format(aba));
        subtotalAbajoTxt.setEditable(false);
        diferenciaTxt.setText(df.format(aba - arr));
        diferenciaTxt.setEditable(false);
    }

    private void volver() {
        VerificarCajasFrame mf = new VerificarCajasFrame(d4, a4, r4);
        mf.setVisible(true);
        this.dispose();
    }

    private void calcular() {
        Double arr = 0.00;
        arr += caja.getTotalFacturadoOf();
        arr += caja.getTotalFacturadoCf();
        Double d1 = Double.valueOf(deudCfAntTxt.getText().replace(",", "."));
        caja.setSaldoDeudorAnteriorCf(d1);
        arr += caja.getSaldoDeudorAnteriorCf();
        d1 = Double.valueOf(deudOfAntTxt.getText().replace(",", "."));
        caja.setSaldoDeudorAnteriorOf(d1);
        arr += caja.getSaldoDeudorAnteriorOf();
        subtotalArribaTxt.setText(df.format(arr));
        subtotalArribaTxt.setEditable(false);
        caja.setSubtotalArriba(arr);
        Double aba = 0.0;
        d1 = Double.valueOf(inicialTxt.getText().replace(",", "."));
        caja.setCajaInicial(d1);
        aba += caja.getCajaInicial();
        aba += caja.getTotalDeudoresHoyCf();
        aba += caja.getTotalDeudoresHoyOf();
        d1 = Double.valueOf(efectivoTxt.getText().replace(",", "."));
        caja.setTotalDeposito(d1);
        aba += caja.getTotalDeposito();
        d1 = Double.valueOf(valesTxt.getText().replace(",", "."));
        caja.setTotalVales(d1);
        aba += caja.getTotalVales();
        caja.setSubtotalAbajo(aba);
        subtotalAbajoTxt.setText(df.format(aba));
        subtotalAbajoTxt.setEditable(false);
        diferenciaTxt.setText(df.format(aba - arr));
        diferenciaTxt.setEditable(false);
    }

    private void guardar() {
        Date fa = caja.getFechaAnterior();
        try {
            fa = sdf.parse(fechaAnteriorTxt.getText());
        } catch (ParseException ex) {
            Logger.getLogger(EditarCajaFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        caja.setFechaAnterior(fa);
        caja.setSaldoDeudorAnteriorCf(Double.valueOf(deudCfAntTxt.getText().replace(",", ".")));
        caja.setSaldoDeudorAnteriorOf(Double.valueOf(deudOfAntTxt.getText().replace(",", ".")));
        caja.setCajaInicial(Double.valueOf(inicialTxt.getText().replace(",", ".")));
        caja.setTotalDeposito(Double.valueOf(efectivoTxt.getText().replace(",", ".")));
        caja.setTotalVales(Double.valueOf(valesTxt.getText().replace(",", ".")));
        caja.setSubtotalArriba(Double.valueOf(subtotalArribaTxt.getText().replace(",", ".")));
        caja.setSubtotalAbajo(Double.valueOf(subtotalAbajoTxt.getText().replace(",", ".")));
        Double dife =  - Double.valueOf(subtotalArribaTxt.getText().replace(",", ".")) 
                + Double.valueOf(subtotalAbajoTxt.getText().replace(",", "."));
        caja.setDiferencia(dife);
        try {
            new StoreService().updateMovimientoCaja(caja);
        } catch (Exception ex) {
            Logger.getLogger(EditarCajaFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "PROCESO REALIZADO");
        volver();
    }
