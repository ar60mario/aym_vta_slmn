    private void llenarTabla() {
        productos = null;
        String f = filtroTxt.getText();
        try {
            productos = new ProductoService().getProductosByIvaCeroAndFiltro(f);
        } catch (Exception ex) {
            Logger.getLogger(ProductosIvaCeroFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(productos != null && !productos.isEmpty()){
            limpiarTabla();
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for(Producto p:productos){
                Object o[] = new Object[4];
                o[0]=p.getCodigo();
                o[1]=p.getDetalle();
                o[2]=p.getRubro().getNombre();
                o[3]=p.getSubRubro().getDetalle();
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void limpiarTabla() {
        int rows = tabla.getRowCount();
        if(rows > 0){
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for(int i=0; i < rows;i++){
                tbl.removeRow(0);
            }
            tabla.setModel(tbl);
        }
    }
