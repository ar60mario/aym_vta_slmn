    private void cambiarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cambiarBtnActionPerformed
        int row = tabla.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN PRODUCTO");
            return;
        }
        producto = productos.get(row);
        producto.setIvaCero(false);
        int a = JOptionPane.showConfirmDialog(this, "CONFIRMA MODIFICAR A IVA 21?", "Atención", JOptionPane.YES_NO_OPTION);
        if(a==0){
            try {
                new ProductoService().updateProducto(producto);
            } catch (Exception ex) {
                Logger.getLogger(ProductosIvaCeroFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            llenarTabla();
        }

    }//GEN-LAST:event_cambiarBtnActionPerformed

