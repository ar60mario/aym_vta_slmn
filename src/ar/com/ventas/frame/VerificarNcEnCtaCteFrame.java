    private void llenarTabla() {
        List<CuentaCorrienteProveedor> ccp = null;
        try {
            ccp = new CuentaCorrienteProveedorService().getAllByProveedor(p);
        } catch (Exception ex) {
            Logger.getLogger(AbTestProveed.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (ccp != null && !ccp.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            Double h = 0.0;
            Double t = 0.0;
            for (CuentaCorrienteProveedor cu : ccp) {
                Double hab = cu.getHaber();
                if (hab > 0) {
                    if (cu.getComprobante() != null) {
                        Long id_p = cu.getComprobante().getId();
                        ComprobanteCompras cc = null;
                        try {
                            cc = new ComprobanteComprasService().getById(id_p);
                        } catch (Exception ex) {
                            Logger.getLogger(VerificarNcEnCtaCteFrame.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        Object o[] = new Object[4];
                        o[0] = cu.getFecha();
                        o[1] = cc.getNumeroFactura();
                        o[2] = cu.getHaber();
                        o[3] = cc.getTotal();
                        h += cu.getHaber();
                        t += cc.getTotal();
                        tbl.addRow(o);
                    }
                }
            }
            Object o[] = new Object[4];
            o[2] = h;
            o[3] = t;
            tbl.addRow(o);
            tabla.setModel(tbl);
        }
    }
