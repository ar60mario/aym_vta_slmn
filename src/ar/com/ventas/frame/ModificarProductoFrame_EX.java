    private void llenarCampos(Producto producto) {
        codigoTxt.setText(String.valueOf(producto.getCodigo()));
        if (producto.getCodigoBarras() != null) {
            codigoBarrasTxt.setText(String.valueOf(producto.getCodigoBarras()));
        } else {
            codigoBarrasTxt.setText("");
        }
        impuestoTxt.setText(String.valueOf(producto.getImpuesto()));
        sugeridoTxt.setText(String.valueOf(producto.getSugerido()));
        precioTxt.setText(String.valueOf(producto.getPrecio()));
        detalleTxt.setText(producto.getDetalle());
        //comboRubro.addItem(producto.getRubro());
        //comboSubRubro.addItem(producto.getSubRubro());
        if (producto.getInactivo()) {
            inactivoChk.setSelected(true);
        } else {
            inactivoChk.setSelected(false);
        }
        int contador = 0;
        for (Rubro rubro : listaRubro) {
            if (rubro.getId() == producto.getRubro().getId()) {
                break;
            }
            contador++;
        }
        comboRubro.setSelectedIndex(contador + 1);
        contador = 0;
        for (SubRubro sub : listaSubRubro) {
            if (sub.getId() == producto.getSubRubro().getId()) {
                break;
            }
            contador++;
        }
        comboSubRubro.setSelectedIndex(contador + 1);
    }

    private void guardarProducto() {
        if (validarCampos()) {
            producto.setCodigo(Integer.valueOf(codigoTxt.getText()));
            if (!codigoBarrasTxt.getText().isEmpty()) {
                producto.setCodigoBarras(Long.valueOf(codigoBarrasTxt.getText()));
            } else {
                producto.setCodigoBarras(null);
            }
            producto.setDetalle(detalleTxt.getText());
            if (!impuestoTxt.getText().isEmpty()) {
                producto.setImpuesto(Float.valueOf(impuestoTxt.getText()));
            } else {
                producto.setImpuesto(Float.valueOf("0.00"));
            }
            if (!sugeridoTxt.getText().isEmpty()) {
                producto.setSugerido(Double.valueOf(sugeridoTxt.getText()));
            } else {
                producto.setSugerido(Double.valueOf("0.00"));
            }
            producto.setPrecio(Double.valueOf(precioTxt.getText()));
            Rubro rubro = producto.getRubro();
            SubRubro sub = producto.getSubRubro();
            int indiceRubro = comboRubro.getSelectedIndex();
            RubroService RubroService = new RubroService();
            try {
                rubro = RubroService.getAllRubros().get(indiceRubro - 1);
                producto.setRubro(rubro);
            } catch (Exception ex) {
                java.util.logging.Logger.getLogger(NuevoProductoFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            int indiceSubRubro = comboSubRubro.getSelectedIndex();
            SubRubroService subRubroService = new SubRubroService();
            try {
                sub = subRubroService.getAllSubRubros().get(indiceSubRubro - 1);
                producto.setSubRubro(sub);
            } catch (Exception ex) {
                java.util.logging.Logger.getLogger(NuevoProductoFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (inactivoChk.isSelected()) {
                producto.setInactivo(true);
            } else {
                producto.setInactivo(false);
            }
            try {
                new ProductoService().updateProducto(producto);
            } catch (Exception ex) {
                java.util.logging.Logger.getLogger(NuevoProductoFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private boolean validarCampos() {
        if (detalleTxt.getText().trim().length() > 39) {
            JOptionPane.showMessageDialog(this, "Largo Producto Superado - " + String.valueOf(detalleTxt.getText().trim().length()));
            return false;
        }
        if (codigoTxt.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Producto - Falta completar Codigo");
            return false;
        }
        if (detalleTxt.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Producto - Falta completar detalle");
            return false;
        }
        if (precioTxt.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Producto - No permitido sin precio");
            return false;
        }
        if (comboRubro.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Producto - Seleccione Rubro");
            return false;
        }
        if (comboSubRubro.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "producto - Seleccione Sub Rubro");
            return false;
        }
        return true;
    }

    private void llenarRubros() {
        try {
            listaRubro = new RubroService().getAllRubros();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se encuentran Rubros para llenar");
        }
        DefaultComboBoxModel model = (DefaultComboBoxModel) comboRubro.getModel();

        if (listaRubro != null && !listaRubro.isEmpty()) {
            for (Rubro rubro : listaRubro) {
                model.addElement(rubro.getNombre());
            }
            comboRubro.setModel(model);
        }
    }

    private void llenarSubRubros() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) comboSubRubro.getModel();
        try {
            listaSubRubro = new SubRubroService().getAllSubRubros();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No se encuentran SubRubros para llenar");
        }
        if (listaSubRubro != null && !listaSubRubro.isEmpty()) {
            for (SubRubro subRubro : listaSubRubro) {
                model.addElement(subRubro.getDetalle());
            }
            comboSubRubro.setModel(model);
        }
    }

    private void limpiarCampos() {
        codigoTxt.setText("");
        codigoBarrasTxt.setText("");
        comboRubro.removeAllItems();
        comboRubro.addItem("");
        comboSubRubro.removeAllItems();
        comboSubRubro.addItem("");
        detalleTxt.setText("");
        precioTxt.setText("0.00");
        sugeridoTxt.setText("0.00");
        impuestoTxt.setText("0.00");
    }
