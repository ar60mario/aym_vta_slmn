    private void verificar() {
        List<Proveedor> proveedores = null;
        List<CuentaCorrienteProveedor> ctaCte;
        try {
            proveedores = new ProveedorService().getAllProveedoresActivosByCodigo();
        } catch (Exception ex) {
            Logger.getLogger(VerificarSaldoProvFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(proveedores != null && !proveedores.isEmpty()){
            for(Proveedor p:proveedores){
                 ctaCte = null;
                try {
                    ctaCte = new CuentaCorrienteProveedorService().getAllByProveedorInverso(p);
                } catch (Exception ex) {
                    Logger.getLogger(VerificarSaldoProvFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                if(ctaCte != null && !ctaCte.isEmpty()){
                    String s1 = df.format(ctaCte.get(0).getSaldo());
                    if(s1.equals("-0,00")){
                        s1="0,00";
                    }
                    String s2 = df.format(p.getSaldo());
                    if(s2.equals("-0,00")){
                        s2="0,00";
                    }
                    if(!(s1.equals(s2))){
                        JOptionPane.showMessageDialog(this, p.getCodigo());
                        System.exit(0);
                    }
                }
            }
        }
    }
