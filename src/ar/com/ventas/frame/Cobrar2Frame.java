    private void llenarDatos() {
        Customer cliente2 = null;
        try {
            cliente2 = new CustomerService().getCustomerByCodigo(cliente.getCodigo());
        } catch (Exception ex) {
            Logger.getLogger(Cobrar2Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
        codigoClienteTxt.setText(cliente2.getCodigo());
        nombreClienteTxt.setText(cliente.getRazonSocial());
        Double doble = 0.00;
        Configuracion conf = null;
        try {
            conf = new ConfiguracionService().getFacturas(1L);
        } catch (Exception ex) {
            Logger.getLogger(Cobrar2Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
        doble = conf.getNumeroFacturaA().doubleValue();
        importeAbonadoTxt.setText(String.valueOf(doble));
    }
