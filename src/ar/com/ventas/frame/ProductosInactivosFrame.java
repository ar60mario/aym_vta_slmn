    private void llenarTabla() {
        codigoTxt.setText("");
        descripcionTxt.setText("");
        productos = null;
        try {
            productos = new ProductoService().getAllProductosOrdenadoByNombreInactivo();
        } catch (Exception ex) {
            Logger.getLogger(ProductosInactivosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (productos != null && !productos.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Producto p : productos) {
                Object o[] = new Object[4];
                o[0] = p.getCodigo();
                o[1] = p.getDetalle();
                o[2] = df.format(p.getPrecio());
                o[3] = df.format(p.getImpuesto());
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void activar() {
        int row = tabla.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un producto para activar");
            return;
        }
        int a = JOptionPane.showConfirmDialog(this, "Texto a confirmar", "Atención", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            Producto p = productos.get(row);
            p.setInactivo(false);
            try {
                new ProductoService().updateProducto(p);
            } catch (Exception ex) {
                Logger.getLogger(ProductosInactivosFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            limpiarTabla();
            llenarTabla();
        }
    }

    private void limpiarTabla() {
        DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
        int r = tbl.getRowCount();
        for (int i = 0; i<r; i++) {
            tbl.removeRow(0);
        }
        tabla.setModel(tbl);
        productos = null;
    }

    private void buscarPorCodigo() {
        if(codigoTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INGRESE UN CODIGO");
            return;
        }
        limpiarTabla();
        productos = null;
        Integer codigo = Integer.valueOf(codigoTxt.getText());
        try {
            productos = new ProductoService().getProductosInactivosByCodigo(codigo);
        } catch (Exception ex) {
            Logger.getLogger(ProductosInactivosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        llenarTabla2();
    }

    private void buscarPorFiltro() {
        if(descripcionTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "INGRESE UN PRODUCTO O PARTE");
            return;
        }
        limpiarTabla();
        productos = null;
        String detalle = descripcionTxt.getText();
        try {
            productos = new ProductoService().getProductosInactivosByFiltro(detalle);
        } catch (Exception ex) {
            Logger.getLogger(ProductosInactivosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        llenarTabla2();
    }

    private void llenarTabla2() {
        if (productos != null && !productos.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Producto p : productos) {
                Object o[] = new Object[4];
                o[0] = p.getCodigo();
                o[1] = p.getDetalle();
                o[2] = df.format(p.getPrecio());
                o[3] = df.format(p.getImpuesto());
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }
