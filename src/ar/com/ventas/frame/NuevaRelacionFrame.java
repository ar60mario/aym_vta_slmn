    private void llenarCombos() {
        comboVendedor.removeAllItems();
        comboVendedor.addItem("");
        vendedores = null;
        try {
            vendedores = new VendedorService().getAllVendedores();
        } catch (Exception ex) {
            Logger.getLogger(NuevaRelacionFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (vendedores != null && !vendedores.isEmpty()) {
            for (Vendedor ve : vendedores) {
                comboVendedor.addItem(ve.getNombre());
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo recuperar los vendedores");
            volver();
        }
        comboRubro.removeAllItems();
        comboRubro.addItem("");
        rubros = null;
        try {
            rubros = new RubroService().getAllRubros();
        } catch (Exception ex) {
            Logger.getLogger(NuevaRelacionFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (rubros != null && !rubros.isEmpty()) {
            for (Rubro ru : rubros) {
                comboRubro.addItem(ru.getNombre());
            }
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo recuperar los rubros");
            volver();
        }
        comboVendedor.requestFocus();
    }

    private void limpiarCampos() {
        porcentualTxt.setText("");
    }

    private void guardar() {
        if (verificar()) {
            PorcentualVendedor pV = new PorcentualVendedor();
            int ve = comboVendedor.getSelectedIndex() -1;
            int ru = comboRubro.getSelectedIndex() -1;
            pV.setVendedor(vendedores.get(ve));
            pV.setRubro(rubros.get(ru));
            pV.setPorcentual(Float.valueOf(porcentualTxt.getText()));
            try {
                new PorcentualVendedorService().savePorcentualVendedor(pV);
            } catch (Exception ex) {
                Logger.getLogger(NuevaRelacionFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        volver();
    }

    private void volver() {
        RelacionVendeListaPorceFrame rvlpf = new RelacionVendeListaPorceFrame();
        rvlpf.setVisible(true);
        this.dispose();
    }

    private boolean verificar() {
        int ve = comboVendedor.getSelectedIndex();
        if (ve < 1) {
            JOptionPane.showMessageDialog(this, "Vendedor");
            return false;
        }
        int ru = comboRubro.getSelectedIndex();
        if (ru < 1){
            JOptionPane.showMessageDialog(this, "Rubro");
            return false;
        }
        if (porcentualTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Porcentual");
            return false;
        }
        Float po = Float.valueOf(porcentualTxt.getText());
        if (po < 0.01){
            JOptionPane.showMessageDialog(this, "Porcentual < que .01");
            return false;
        }
        return true;
    }
