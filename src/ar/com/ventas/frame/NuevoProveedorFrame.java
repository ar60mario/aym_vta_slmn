    private void guardar() {
        if (verificarDuplicado()) {
            if (validarCampos()) {
                Proveedor proveedor = new Proveedor();
                proveedor.setMail(mailTxt.getText());
                proveedor.setObservaciones(obsTxt.getText());
                proveedor.setRazonSocial(razonSocialTxt.getText());
                proveedor.setCodigo(Integer.valueOf(codigoTxt.getText()));
                proveedor.setCategoriaIva(comboCategoria.getSelectedIndex());
                proveedor.setCuit(cuitTxt.getText());
                proveedor.setTelefono(telTxt.getText());
                proveedor.setCelular(celularTxt.getText());
                proveedor.setActivo(true);
                proveedor.setSaldo(0.0);
                proveedor.setContacto(contactoTxt.getText());
                if(llevaStockChk.isSelected()){
                    proveedor.setStock(true);
                }else{
                    proveedor.setStock(false);
                }
                try {
                    new ProveedorService().saveProveedor(proveedor);
                    JOptionPane.showMessageDialog(this, "PROVEEDOR guardado correctamente.");
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Proveedor - No se puede guardar proovedor");
                    Logger.getLogger(NuevoProveedorFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                limpiarCampos();
                llenarCombo();
            }
        }else{
            codigoTxt.setText("");
            codigoTxt.requestFocus();
        }
    }

    private void limpiarCampos() {
        celularTxt.setText("");
        codigoTxt.setText("");
        cuitTxt.setText("");
        comboCategoria.removeAllItems();
        comboCategoria.addItem("");
        mailTxt.setText("");
        obsTxt.setText("");
        razonSocialTxt.setText("");
        telTxt.setText("");
        contactoTxt.setText("");
        llevaStockChk.setSelected(true);
    }

    private void llenarCombo() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) comboCategoria.getModel();
        model.addElement("1- Inscripto");
        model.addElement("2- Monotributo");
        model.addElement("3- Exento");
        model.addElement("4- Consumidor Final");
        comboCategoria.setModel(model);
    }

    private boolean validarCampos() {
        if (razonSocialTxt.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Proveedor - Falta completar la razon social");
            return false;
        }
        if (codigoTxt.getText().trim().length() == 0) {
            JOptionPane.showConfirmDialog(this, "Proveedor - Falta completar el codigo");
            return false;
        }
        if (comboCategoria.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Proveedor - Seleccione categor√≠a de iva");
            return false;
        }
//        if (calleTxt.getText().trim().length() == 0) {
//            JOptionPane.showMessageDialog(this, "Proveedor - Falta completar nombre de la calle");
//            return false;
//        }
//        if (numeroTxt.getText().trim().length() == 0) {
//            JOptionPane.showMessageDialog(this, "Proveedor - Falta completar numero de la direccion");
//            return false;
//        }
        if (cuitTxt.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Proveedor - Falta completar cuit ");
            return false;
        }
        if (cuitTxt.getText().trim().length() > 13) {
            JOptionPane.showMessageDialog(this, "CUIT erroneo ");
            return false;
        }
        return true;
    }

    private boolean verificarDuplicado() {
        Boolean libre = true;
        if (!codigoTxt.getText().isEmpty()) {
            int cod = Integer.valueOf(codigoTxt.getText());
            Proveedor p = null;
            try {
                p = new ProveedorService().getProveedorByCodigo(cod);
                if (p != null) {
                    libre = false;
                }
            } catch (Exception ex) {
                Logger.getLogger(ModificarProveedorFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (libre) {
            return true;
        } else {
            JOptionPane.showMessageDialog(this, "N√∫mero Proveedor existente");
            return false;
        }
    }
