    private void colocarFecha() {
        fechaTxt.setText(sdf.format(new Date()));
    }

    private void llenarTabla() {
        limpiarTablas();
        recibos = null;
        recibis = null;
        Date f = null;
        try {
            f = sdf.parse(fechaTxt.getText());
        } catch (ParseException ex) {
            Logger.getLogger(ListarRecibosFrame.class.getName()).log(Level.SEVERE, null, ex);
            return;
        }
        if (f != null) {
            try {
                recibos = new ReciboService().getRecibosByFecha(f);
            } catch (Exception ex) {
                Logger.getLogger(ListarRecibosFrame.class.getName()).log(Level.SEVERE, null, ex);
                return;
            }
        }
        if (recibos != null && !recibos.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tablaRecibos.getModel();
            Double t = 0.0;
            for (Recibo r : recibos) {
                Object ob[] = new Object[5];
                ob[0] = r.getNumero();
                ob[1] = r.getCliente().getCodigo();
                ob[2] = r.getCliente().getRazonSocial();
                ob[3] = df.format(r.getImporte());
                ob[4]=df.format(r.getCliente().getSaldo());
                t += r.getImporte();
                tbl.addRow(ob);
            }
            Object ob[] = new Object[5];
            ob[1] = "TOTAL";
            ob[3] = df.format(t);
            tbl.addRow(ob);
            tablaRecibos.setModel(tbl);
        }
        try {
            recibis = new PaymentService().getPaymentByDia(f);
        } catch (Exception ex) {
            Logger.getLogger(ListarRecibosFrame.class.getName()).log(Level.SEVERE, null, ex);
            return;
        }
        if (recibis != null && !recibis.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tablaRec.getModel();
            Double t = 0.0;
            for (Payment r : recibis) {
                Object ob[] = new Object[5];
                ob[0] = r.getNumero();
                ob[1] = r.getCliente().getCodigo();
                String c = r.getCliente().getCodigo();
                String nombre = null;
                try {
                    nombre = new ClienteService().getClienteByCodigo(c).getRazonSocial();
                } catch (Exception ex) {
                    Logger.getLogger(ListarRecibosFrame.class.getName()).log(Level.SEVERE, null, ex);
                    return;
                }
                ob[2] = nombre;
                ob[3] = df.format(r.getImporte());
                ob[4]=df.format(r.getCliente().getSaldo());
                t += r.getImporte();
                tbl.addRow(ob);
            }
            Object ob[] = new Object[5];
            ob[1] = "TOTAL";
            ob[3] = df.format(t);
            tbl.addRow(ob);
            tablaRec.setModel(tbl);
        }
    }

    private void volver() {
        MainFrame mf = new MainFrame();
        mf.setVisible(true);
        this.dispose();
    }

    private void limpiarTablas() {
        DefaultTableModel tbl0 = (DefaultTableModel) tablaRecibos.getModel();
        DefaultTableModel tbl1 = (DefaultTableModel) tablaRec.getModel();
        int a = tbl0.getRowCount();
        int b = tbl1.getRowCount();
        if(a>0){
            for(int i=0;i<a;i++){
                tbl0.removeRow(0);
            }
            tablaRecibos.setModel(tbl0);
        }
        if(b>0){
            for(int i=0;i<b;i++){
                tbl1.removeRow(0);
            }
            tablaRec.setModel(tbl1);
        }
    }
