    private void llenarTabla() {
        limpiarTabla();
        productos = null;
        try {
            productos = new ProductoService().getAllProductosActivosPrecioCero();
        } catch (Exception ex) {
            Logger.getLogger(ProductosEnCeroFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (productos != null && !productos.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Producto p : productos) {
                Object o[] = new Object[5];
                o[0] = p.getCodigo();
                o[1] = p.getCodigoBarras();
                o[2] = p.getDetalle();
                o[3] = p.getRubro().getNombre();
                o[4] = p.getSubRubro().getDetalle();
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void limpiarTabla() {
        int rows = tabla.getRowCount();
        if (rows > 0) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (int i = 0; i < rows; i++) {
                tbl.removeRow(0);
            }
            tabla.setModel(tbl);
        }
    }
