    private void llenarTabla() {
        porcentuales = null;
        try {
            porcentuales = new PorcentualVendedorService().getAllPorcentualVendedor();
        } catch (Exception ex) {
            Logger.getLogger(RelacionVendeListaPorceFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (porcentuales != null && !porcentuales.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tablaVendePorcentual.getModel();
            for (PorcentualVendedor pv : porcentuales) {
                Object ob[] = new Object[4];
                ob[0] = pv.getVendedor().getCodigo();
                ob[1] = pv.getVendedor().getNombre();
                ob[2] = pv.getRubro().getNombre();
                ob[3] = df.format(pv.getPorcentual());
                tbl.addRow(ob);
            }
            tablaVendePorcentual.setModel(tbl);
        }
    }

    private void nuevo() {
        NuevaRelacionFrame nrf = new NuevaRelacionFrame();
        nrf.setVisible(true);
        this.dispose();
    }

    private void modificar() {
        int row = tablaVendePorcentual.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar uno para Modificar");
            return;
        }
        Long id = porcentuales.get(row).getId();
        ModificarRelacionFrame mrf = new ModificarRelacionFrame(id);
        mrf.setVisible(true);
        this.dispose();
    }

    private void volver() {
        MainFrame mf = new MainFrame();
        mf.setVisible(true);
        this.dispose();
    }

    private void eliminar() {
        int row = tablaVendePorcentual.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar uno para Eliminar");
            return;
        }
        Long id = porcentuales.get(row).getId();
        int a = JOptionPane.showConfirmDialog(this, "Confirma eliminar Porcentual Vendedor?",
                 "Atención", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            PorcentualVendedor p = porcentuales.get(row);
            try {
                new PorcentualVendedorService().deletePorcentualVendedor(p);
                JOptionPane.showMessageDialog(this, "ELIMINADO CORRECTAMENTE");
            } catch (Exception ex) {
                Logger.getLogger(RelacionVendeListaPorceFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        limpiarTabla();
        llenarTabla();
    }

    private void limpiarTabla() {
        int rows = tablaVendePorcentual.getRowCount();
        if (rows > 0) {
            DefaultTableModel tbl = (DefaultTableModel) tablaVendePorcentual.getModel();
            for (int i = 0; i < rows; i++) {
                tbl.removeRow(0);
            }
            tablaVendePorcentual.setModel(tbl);
        }
    }
