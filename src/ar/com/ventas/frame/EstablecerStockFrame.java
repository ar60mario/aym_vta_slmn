    private void llenarFrame() {
        codigoTxt.setText(producto.getCodigo().toString());
        descripcionTxt.setText(producto.getDetalle());
        if (producto.getStock() != null) {
            actualTxt.setText(df.format(producto.getStock()));
        } else {
            actualTxt.setText(df.format(0.0));
        }
        if (producto.getStockMinimo() != null) {
            minimoTxt.setText(df.format(producto.getStockMinimo()));
        } else {
            minimoTxt.setText(df.format(0.0));
        }
        codigoTxt.setEditable(false);
        descripcionTxt.setEditable(false);
        actualTxt.setEditable(false);
        minimoTxt.setEditable(false);
        nActualTxt.setText("");
        nMinimoTxt.setText("");
        nActualTxt.requestFocus();
    }

    private void grabar() {
        Float antes = 0F;
        if (producto.getStock() != null) {
            antes = producto.getStock();
        }
        Float a = 0F;
        if (!nActualTxt.getText().isEmpty()) {
            a = Float.valueOf(nActualTxt.getText());
            producto.setStock(a);
        }
        if (!nMinimoTxt.getText().isEmpty()) {
            Float m = Float.valueOf(nMinimoTxt.getText());
            producto.setStockMinimo(m);
        }
        int op = JOptionPane.showConfirmDialog(null, "Confirma cambiar los valores Originales?",
                "MODIFICAR VALORES STOCK",
                JOptionPane.YES_NO_OPTION);
        if (op == 0) {
            try {
                new ProductoService().updateProducto(producto);
            } catch (Exception ex) {
                Logger.getLogger(EstablecerStockFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            Integer co = 1;
            ContadorCpbteStock con = null;
            try {
                con = new ContadorCpbteStockService().getFacturas(co);
            } catch (Exception ex) {
                Logger.getLogger(IngresarStockConFacturaFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            long nf = 0L;
            if (con != null) {
                if (con.getComprobante() != null) {
                    nf = con.getComprobante() + 2;
                } else {
                    nf += 2;
                }
            }
            con.setComprobante(nf);
            try {
//                new ContadorCpbteStockService().updateConfiguracion(con);
            } catch (Exception ex) {
                Logger.getLogger(IngresarStockConFacturaFrame.class.getName()).log(Level.SEVERE, null, ex);
//                    JOptionPane.showMessageDialog(this, "ERROR por favor REINTENTE");
//                    return;
            }
            CtaCteStock ccs = new CtaCteStock();
            ccs.setCantidad(-antes);
            ccs.setFecha(new Date());
            ccs.setFechaSistema(new Date());
            ccs.setComprobante(nf);
            ccs.setProducto(producto);
//            ccs.setProveedor(usuario);
            ccs.setUsuario(u);
            try {
//                new CtaCteStockService().saveCtaCteStock(ccs);
            } catch (Exception ex) {
                Logger.getLogger(EstablecerStockFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            ccs = new CtaCteStock();
            ccs.setCantidad(a);
            ccs.setFecha(new Date());
            ccs.setFechaSistema(new Date());
            ccs.setComprobante(nf +1);
            ccs.setProducto(producto);
            ccs.setUsuario(u);
            try {
//                new CtaCteStockService().saveCtaCteStock(ccs);
            } catch (Exception ex) {
                Logger.getLogger(EstablecerStockFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            volver();
        }
    }
