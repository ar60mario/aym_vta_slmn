    private void verificar() {
        List<Customer> clientes = null;
        try {
            clientes = new CustomerService().getAllCustomers();
        } catch (Exception ex) {
            Logger.getLogger(TestCustomerFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (clientes != null && !clientes.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Customer cliente : clientes) {
                Object o[] = new Object[4];
                Double a = cliente.getSaldo();
                Double b = 0.0;
                o[0] = cliente.getCodigo();
                o[1] = df.format(a);
                Inventory inve = null;
                try {
                    inve = new InventoryService().getUltimoMovimiento(cliente);
                } catch (Exception ex) {
                    Logger.getLogger(TestCustomerFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (inve != null) {
                    b = inve.getSaldo();
                } else {
                    b = 0.0;
                }
                o[2] = df.format(b);
                String c = df.format(cliente.getSaldo());
                String d = df.format(b);
                if (c.equals(d)) {
                    o[3] = "IGUAL";
                } else {
                    o[3] = "-";
                }
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }
