    private void limpiarCampos() {
        numeroFacturaTxt.setText("");
        nombreClienteTxt.setText("");
        importeFacturaTxt.setText("");
        confirmarFacturaBtn.setEnabled(false);
    }

    private void buscarFactura() {
        // if (!letraFacturaTxt.getText().isEmpty()) {
        //   if (!sucursalFacturaTxt.getText().isEmpty()) {
        if (!numeroFacturaTxt.getText().isEmpty()) {
            String letra = "B";
            Integer sucursal = 1;
            Integer numero = Integer.valueOf(numeroFacturaTxt.getText());
            Boolean si = false;
            factura = null;
            try {
                factura = new FacturaService().getFacturaByNumero(letra, sucursal, numero);
                si = true;
            } catch (Exception ex) {
                Logger.getLogger(NotaCreditoPedirFactura.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (si) {
                if (factura != null) {
                    customer = factura.getCustomer();
                    try {
                        cliente = new ClienteService().getClienteByCodigo(customer.getCodigo());
                    } catch (Exception ex) {
                        Logger.getLogger(NotaCreditoPedirFactura.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    String codigo = cliente.getCodigo();
                    ClienteTraba ct = null;
                    CustomerTraba cuTr = null;
                    try {
                        ct = new ClienteTrabaService().getClienteByCodigo(codigo);
                        cuTr = new CustomerTrabaService().getClienteByCodigo(codigo);
                    } catch (Exception ex) {
                        Logger.getLogger(NotaCreditoPedirCliente.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    if (ct.getTraba1() != null) {
                        if (ct.getTraba1()) {
                            JOptionPane.showMessageDialog(this, "Cliente bloqueado para esta Operación.");
                            confirmarFacturaBtn.setEnabled(false);
                            numeroFacturaTxt.requestFocus();
                            nombreClienteTxt.setText("");
                            return;
                        }
                    }
                    if (cuTr.getTraba2() != null) {
                        if (cuTr.getTraba2()) {
                            JOptionPane.showMessageDialog(this, "Cliente bloqueado para esta Operación.");
                            confirmarFacturaBtn.setEnabled(false);
                            numeroFacturaTxt.requestFocus();
                            nombreClienteTxt.setText("");
                            return;
                        }
                    }
                    nombreClienteTxt.setText(cliente.getRazonSocial());
                    importeFacturaTxt.setText(String.valueOf(df.format(factura.getTotal())));
                    confirmarFacturaBtn.setEnabled(true);
                    confirmarFacturaBtn.requestFocus();
                } else {
                    nombreClienteTxt.setText("No se encuentra Factura");
                    confirmarFacturaBtn.setEnabled(false);
                    numeroFacturaTxt.requestFocus();
                }
            }
        }
    }
