    private void codigoTxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codigoTxtKeyPressed
        if (evt.getKeyCode() == 10) {
            if (!codigoTxt.getText().isEmpty()) {
                int cod = Integer.valueOf(codigoTxt.getText());
                try {
                    usuario = new UsuarioService().getUsuarioByCodigo(cod);
                } catch (Exception ex) {
                    Logger.getLogger(A0AccesoUsuarioFrame.class.getName()).log(Level.SEVERE, null, ex);
                    usuario = null;
                }
                if (usuario != null) {
                    nombreTxt.setText(usuario.getNombre());
                    pswTxt.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(this, "Error en C칩digo Usuario");
                    codigoTxt.setText("");
                    codigoTxt.requestFocus();
                }
                if (!pswTxt.getText().isEmpty()) {
                    Integer psw = Integer.valueOf(pswTxt.getText());
                    if (psw.equals(usuario.getContrasena())) {

                    }
                }
            }
        }
    }//GEN-LAST:event_codigoTxtKeyPressed

