    private void go() {
        Date fe = new Date();
        try {
            fe = sdf.parse(fechaTxt.getText());
        } catch (ParseException ex) {
            Logger.getLogger(A0TstSaldoCteFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "ERROR FECHA");
            return;
        }
        Double totalFcOf = 0.00;
        Double totalFcCf = 0.00;
        Double totalRc = 0.00;
        Double totalPy = 0.00;
        Double totalFcOfCaja = 0.00;
        Double totalFcCfCaja = 0.00;
        Double totalFcOfCajaAnterior = 0.00;
        Double totalFcCfCajaAnterior = 0.00;
        Double totalFcOfCajaHoy = 0.00;
        Double totalFcCfCajaHoy = 0.00;
        List<IvaVentas> iv = null;
        try {
            iv = new IvaVentasService().getFactByFecha(fe);
        } catch (Exception ex) {
            Logger.getLogger(A0TstSaldoCteFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (iv != null && !iv.isEmpty()) {
            for (IvaVentas i : iv) {
                totalFcOf += i.getTotal();
            }
        }
        List<Activity> ac = null;
        try {
            ac = new ActivityService().getFcByFecha(fe);
        } catch (Exception ex) {
            Logger.getLogger(A0TstSaldoCteFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (ac != null && !ac.isEmpty()) {
            for (Activity a : ac) {
                totalFcCf += a.getTotal();
            }
        }
        List<Recibo> rc = null;
        try {
            rc = new ReciboService().getRecibosByFecha(fe);
        } catch (Exception ex) {
            Logger.getLogger(A0TstSaldoCteFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (rc != null && !rc.isEmpty()) {
            for (Recibo r : rc) {
                totalRc += r.getImporte();
            }
        }
        List<Payment> py = null;
        try {
            py = new PaymentService().getPaymentByDia(fe);
        } catch (Exception ex) {
            Logger.getLogger(A0TstSaldoCteFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(py != null && !py.isEmpty()){
            for(Payment p:py){
                totalPy += p.getImporte();
            }
        }
        Stores st = null;
        try {
            st = new StoreService().getMovimientoCajaByFecha(fe);
        } catch (Exception ex) {
            Logger.getLogger(A0TstSaldoCteFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (st != null) {
            totalFcOfCaja = st.getTotalFacturadoOf();
            totalFcCfCaja = st.getTotalFacturadoCf();
            totalFcOfCajaAnterior = st.getSaldoDeudorAnteriorOf();
            totalFcCfCajaAnterior = st.getSaldoDeudorAnteriorCf();
            totalFcOfCajaHoy = st.getTotalDeudoresHoyOf();
            totalFcCfCajaHoy = st.getTotalDeudoresHoyCf();
        }
        Double totalSaldosCtaCteHoy = 0.00;
        Double totalSaldosInventHoy = 0.00;
        CtaCteCliente ccc;
        Inventory inv = null;
        List<Cliente> clientes = null;
        try {
            clientes = new ClienteService().getAllClientes();
        } catch (Exception ex) {
            Logger.getLogger(A0TstSaldoCteFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (clientes != null && !clientes.isEmpty()) {
            for (Cliente cli : clientes) {
                ccc = null;
                try {
                    ccc = new CtaCteClienteService().getUltimoMovimientoPorDia(cli,fe);
                } catch (Exception ex) {
                    //Logger.getLogger(A0TstSaldoCteFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                if(ccc != null){
                    totalSaldosCtaCteHoy += ccc.getSaldo();
                }
            }
        }
        List<Customer> customers = null;
        try {
            customers = new CustomerService().getAllCustomers();
        } catch (Exception ex) {
            Logger.getLogger(A0TstSaldoCteFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(customers != null && !customers.isEmpty()){
            for(Customer c:customers){
                inv = null;
                try {
                    inv = new InventoryService().getUltimoMovimientoPorDia(c, fe);
                } catch (Exception ex) {
                    //Logger.getLogger(A0TstSaldoCteFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                if(inv != null){
                    totalSaldosInventHoy += inv.getSaldo();
                }
            }
        }
        System.out.println("Total Fc OF          "+df.format(totalFcOf));
        System.out.println("Total Fc CF          "+df.format(totalFcCf));
        System.out.println("Total Rc Of          "+df.format(totalRc));
        System.out.println("Total Rc CF          "+df.format(totalPy));
        System.out.println("Total Sa CC OF Hoy   "+df.format(totalSaldosCtaCteHoy));
        System.out.println("Total Sa CC CF Hoy   "+df.format(totalSaldosInventHoy));
        System.out.println("Total Fc Caja OF     "+df.format(totalFcOfCaja));
        System.out.println("Total Fc Caja CF     "+df.format(totalFcCfCaja));
        System.out.println("Total Fc Caja OF ant "+df.format(totalFcOfCajaAnterior));
        System.out.println("Total Fc Caja CF ant "+df.format(totalFcCfCajaAnterior));
        System.out.println("Total Fc Caja OF hoy "+df.format(totalFcOfCajaHoy));
        System.out.println("Total Fc Caja CF hoy "+df.format(totalFcCfCajaHoy));
        
        
        /*
        Double  = 0.00;
        Double  = 0.00;
        Double  = 0.00;
        Double  = 0.00;
        Double  = 0.00;
        Double  = 0.00;
        Double  = 0.00;
        Double  = 0.00;
        Double  = 0.00;
        */
    }
