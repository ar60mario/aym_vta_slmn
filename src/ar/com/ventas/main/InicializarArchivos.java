    private void inicializarArchivos() {
        Routines conf = new Routines();
        conf.setIva((float) 21);
        conf.setNumeroFactura(1);
        conf.setSucursal(1);
        
        try {
            new RoutinesService().saveRoutines(conf);
            JOptionPane.showMessageDialog(this,"El Archivo Configuración fue Creado Correctamente.");
        } catch (Exception ex) {
            Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "El Archivo Configuración Creado con Anterioridad.",
                        "Atencion",
                        JOptionPane.WARNING_MESSAGE);
        }
    }

    private void crearClientes() {
        List<Cliente> clientes = null;
        try {
            clientes = new ClienteService().getAllClientes();
        } catch (Exception ex) {
            Logger.getLogger(InicializarArchivos.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (Cliente cli: clientes) {
            Customer cus = new Customer();
            cus.setCodigo(cli.getCodigo());
            cus.setActivo(true);
            cus.setDescuento(0.0F);
            cus.setObservaciones("");
            cus.setSaldo(0.00);
            cus.setTieneDescuento(false);
            try {
                new CustomerService().saveCustomer(cus);
            } catch (Exception ex) {
                Logger.getLogger(InicializarArchivos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
