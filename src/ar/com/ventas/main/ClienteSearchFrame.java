    public ClienteSearchFrame() {
        initComponents();
        List<ClienteTraba> clientes = null;
        String cod = "";
        try {
            clientes = new ClienteTrabaService().getListadoClienteTraba();
        } catch (Exception ex) {
            Logger.getLogger(ClienteSearchFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        for(ClienteTraba c:clientes){
            if(c.getCodigo().equals(cod)){
                System.out.println(c.getCodigo());
                JOptionPane.showMessageDialog(this, c.getCodigo());
            }else{
                cod = c.getCodigo();
            }
        }
        String cad = "";
        List<CustomerTraba> cientes = null;
        try {
            cientes = new CustomerTrabaService().getListaTrabas();
        } catch (Exception ex) {
            Logger.getLogger(ClienteSearchFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        for(CustomerTraba c:cientes){
            if(c.getCodigo().equals(cad)){
                System.out.println(c.getCodigo());
                JOptionPane.showMessageDialog(this, c.getCodigo());
            }else{
                cad = c.getCodigo();
            }
        }
        
        List<Customer> customers = null;
        try {
            customers = new CustomerService().getCustomerOrdenado();
        } catch (Exception ex) {
            Logger.getLogger(ClienteSearchFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        String cud = "";
        for(Customer c:customers){
            if(c.getCodigo().equals(cud)){
                System.out.println(c.getCodigo());
                JOptionPane.showMessageDialog(this, c);
            }else{
                cud = c.getCodigo();
            }
        }
    }

